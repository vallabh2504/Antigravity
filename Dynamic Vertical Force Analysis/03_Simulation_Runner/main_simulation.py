def plot_results(results: dict, save_dir: str = PLOT_DIR):
    try:
        import matplotlib.pyplot as plt
        import matplotlib.gridspec as gridspec

        fig = plt.figure(figsize=(16, 10))
        gs  = gridspec.GridSpec(3, 3, figure=fig, hspace=0.45, wspace=0.35)

        t = results['time_s']

        def _ax_plot(ax, y, title, unit, color='steelblue'):
            ax.plot(t, y, lw=0.4, color=color, alpha=0.85)
            ax.set_title(title, fontsize=9)
            ax.set_ylabel(unit, fontsize=8)
            ax.set_xlabel('Time [s]', fontsize=8)
            ax.tick_params(labelsize=7)
            ax.grid(True, alpha=0.3)

        _ax_plot(fig.add_subplot(gs[0, 0]), results['speed_kmh'],   'Speed',          'km/h', 'navy')
        _ax_plot(fig.add_subplot(gs[0, 1]), results['ax_ms2'],      'Long. Accel.',   'm/s²', 'darkorange')
        _ax_plot(fig.add_subplot(gs[0, 2]), np.clip(results['turn_radius_m'], -200, 200),
                                                                     'Turn Radius',    'm',    'purple')

        _ax_plot(fig.add_subplot(gs[1, 0]), results['Fz_outer_N'],  'Fz Outer',       'N',    'crimson')
        _ax_plot(fig.add_subplot(gs[1, 1]), results['Fy_outer_N'],  'Fy Outer',       'N',    'darkcyan')
        _ax_plot(fig.add_subplot(gs[1, 2]), results['Fx_outer_N'],  'Fx Outer',       'N',    'darkgreen')

        _ax_plot(fig.add_subplot(gs[2, 0]), results['Mx_outer_Nm'], 'Mx Outer',       'N·m',  'saddlebrown')
        _ax_plot(fig.add_subplot(gs[2, 1]), results['My_outer_Nm'], 'My Outer',       'N·m',  'darkolivegreen')
        _ax_plot(fig.add_subplot(gs[2, 2]), results['Fz_dyn_outer_N'], 'Fz Dynamic',  'N',    'darkviolet')

        fig.suptitle('Shell Eco-Marathon — Silesia Ring — 35-min Fatigue Load History',
                     fontsize=12, fontweight='bold')

        out_png = os.path.join(save_dir, 'fatigue_loads_summary.png')
        plt.savefig(out_png, dpi=150, bbox_inches='tight')
        print(f"\nSummary plot saved: {out_png}")
        plt.show()
    except ImportError:
        print("(matplotlib not available for plotting)")


