    #"""Force fatigue along Z-axis (Normal)"""
    def solve_longitudinal(self):
        #"""Calculates pitch shift and longitudinal normal loads."""
        # Pitch shift formula due to acceleration and load_difference due to l_f & l_r
        self.p.ra_Fz_pitch = (((self.p.m * self.g * np.sin(self.p.inc_angle) + self.p.m * self.p.ax) * self.p.cg_hei) + self.p.m * self.g * np.cos(self.p.inc_angle) * self.p.l_f) / self.p.wb  # Moment about FA
        self.p.fa_Fz_pitch = self.p.m * self.g * np.cos(self.p.inc_angle) - self.p.ra_Fz_pitch   # balancing Vertical forces w.r.t the inclination
        # FIX #7: delta_Fz_pitch retained as a diagnostic output (was dead code in V1, kept for reference)
        self.p.delta_Fz_pitch = (self.p.ra_Fz_pitch - self.p.fa_Fz_pitch) / 2

    def solve_lateral(self):
        #"""Calculates lateral normal loads and stiffness required due to cornering"""

        # FIX #8: Robust float-safe guard — replaces fragile `== 0` equality check.
        # Prevents ZeroDivisionError from ay = v²/|R| when turn_radius → 0,
        # and prevents tan(roll_angle) singularity for near-zero roll angles.
        if abs(self.p.roll_angle) < 1e-9 or abs(self.p.turn_radius) < 1e-9:
            return

        # Roll Torque/Moment calculation
        total_angle = self.p.roll_angle + self.p.bank_angle
        self.p.ay = (self.p.velo_v**2) / abs(self.p.turn_radius)            # lateral acceleration
        torque = self.p.m_spr * self.p.del_h * (self.g * np.sin(total_angle) + self.p.ay * np.cos(total_angle))
        # Stiffness derivation
        ratio = self.p.m_ra_spr / self.p.m_fa_spr
        self.p.cf = (2 * torque) / ((self.p.s_f**2) * np.tan(self.p.roll_angle) * (1 + ratio))
        self.p.cr = self.p.cf * ratio * (self.p.s_f**2 / self.p.s_r**2)

        # Static load shift due to bank-angle and pitch (ax)
        ra_Fz_static_per_wheel = (self.p.ra_Fz_pitch * np.cos(self.p.bank_angle)) / 2       # (Vertical forces w.r.t banked road / 2) is the static load on each wheel of RA
        fa_Fz_static_per_wheel = (self.p.fa_Fz_pitch * np.cos(self.p.bank_angle)) / 2       # (Vertical forces w.r.t banked road / 2) is the static load on each wheel of FA

        # Dynamic load shift lumped mass method
        self.p.fa_delta_Fz_roll_lm = (self.p.cf * self.p.s_f * np.tan(self.p.roll_angle) / 2)
        self.p.ra_delta_Fz_roll_lm = (self.p.cr * self.p.s_r * np.tan(self.p.roll_angle) / 2)

        # FIX #5: Bank-corrected effective lateral acceleration for unsprung mass centrifugal term.
        # V1 used raw ay = v²/R for the unsprung mass terms, which omitted the gravity component
        # of the banked road acting on the unsprung masses.
        # Correct effective lateral acceleration perpendicular to road surface:
        #   ay_eff = ay × cos(bank_angle) − g × sin(bank_angle)
        # (sign convention: bank_angle positive when road tilts in the same direction as cornering)
        # For bank_angle = 0 this is identical to V1 behaviour.
        ay_eff_unspr = self.p.ay * np.cos(self.p.bank_angle) - self.g * np.sin(self.p.bank_angle)

        # Dynamic load shift distributed mass method
        self.p.fa_delta_Fz_roll = (2 * ay_eff_unspr / self.p.s_f * (self.p.m_fa_unspr * self.p.r_unloaded + self.p.m_fa_spr * self.p.fa_rp) + self.p.cf * self.p.s_f * np.tan(self.p.roll_angle)) / 2
        self.p.ra_delta_Fz_roll = (2 * ay_eff_unspr / self.p.s_r * (self.p.m_ra_unspr * self.p.r_unloaded + self.p.m_ra_spr * self.p.ra_rp) + self.p.cr * self.p.s_r * np.tan(self.p.roll_angle)) / 2

        # Normal loads (Force = Static + Dynamic)
        self.p.ra_Fz_out = ra_Fz_static_per_wheel + self.p.ra_delta_Fz_roll  # RA outer wheel
        self.p.ra_Fz_inn = ra_Fz_static_per_wheel - self.p.ra_delta_Fz_roll  # RA inner wheel
        self.p.ra_Fz_total = self.p.ra_Fz_out + self.p.ra_Fz_inn             # RA total load

        self.p.fa_Fz_out = fa_Fz_static_per_wheel + self.p.fa_delta_Fz_roll  # FA outer wheel
        self.p.fa_Fz_inn = fa_Fz_static_per_wheel - self.p.fa_delta_Fz_roll  # FA inner wheel
        self.p.fa_Fz_total = self.p.fa_Fz_out + self.p.fa_Fz_inn             # FA total load

        # mapping wheels with inner and outer: Right Turn = Positive radius, Left Turn = Negative Turn
        if self.p.turn_radius > 0:
            # Right Turn: Left side is Outer
            self.p.fa_Fz_left = self.p.fa_Fz_out
            self.p.fa_Fz_right = self.p.fa_Fz_inn
            self.p.ra_Fz_left = self.p.ra_Fz_out
            self.p.ra_Fz_right = self.p.ra_Fz_inn
        else:
            # Left Turn: Right side is Outer
            self.p.fa_Fz_left = self.p.fa_Fz_inn
            self.p.fa_Fz_right = self.p.fa_Fz_out
            self.p.ra_Fz_left = self.p.ra_Fz_inn
            self.p.ra_Fz_right = self.p.ra_Fz_out

    def vertical_force_fatigue(self):
        self.solve_longitudinal()
        self.solve_lateral()

