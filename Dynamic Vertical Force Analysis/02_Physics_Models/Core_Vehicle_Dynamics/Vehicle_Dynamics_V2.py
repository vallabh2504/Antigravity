    #"""Force fatigue along X-axis (longitudinal)"""
    def solve_propulsion(self):
        # FIX #1: Correct open-differential model — symmetrically assigns drive force to the
        # INNER (lighter) wheel regardless of turn direction.
        #
        # V1 BUG: The code always computed r_dyn from ra_Fz_left and compared ra_Fz_right vs
        # ra_Fz_left to decide which wheel is driven. This was only correct for LEFT turns
        # (turn_radius < 0) where ra_Fz_left = ra_Fz_inn (inner/lighter). For RIGHT turns
        # (turn_radius > 0) ra_Fz_left = ra_Fz_out (outer/heavier), so the else-branch
        # incorrectly sent drive to the heavier outer wheel — simulating a locked differential
        # for right turns but an open differential for left turns.
        #
        # FIX: Explicitly identify the INNER wheel from turn direction, use its Fz for r_dyn,
        # and always assign ra_Fx_inn = ra_Fx_driven, ra_Fx_out = 0.
        if self.p.turn_radius > 0:
            # Right Turn: LEFT = outer, RIGHT = inner
            Fz_inner = self.p.ra_Fz_right
        else:
            # Left Turn: RIGHT = outer, LEFT = inner
            Fz_inner = self.p.ra_Fz_left

        r_dyn = self.dynamic_wheel(Fz_inner)    # dynamic radius at the driven (inner) wheel
        self.p.ra_Fx_driven = self.p.motor_torque / r_dyn[0]
        self.p.driven_tire_wavelength_lim = r_dyn[1]

        # Open differential: all drive torque goes to the inner (lighter/less-loaded) wheel
        self.p.ra_Fx_inn = self.p.ra_Fx_driven   # rolling resistance considered in Moment_y <$%#>
        self.p.ra_Fx_out = 0.0

