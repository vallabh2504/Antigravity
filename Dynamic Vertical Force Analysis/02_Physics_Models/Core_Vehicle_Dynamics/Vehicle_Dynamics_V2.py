    #"""Force fatigue along Y-axis (Lateral)"""
    def kremple(self, Fz, lon_slip, alpha_deg):
        # PAC89 Tyre Coefficients — SOURCE NOTE (L3 audit fix):
        # Lateral coefficients (a) and longitudinal coefficients (b) are representative
        # values for a small-car tyre in the 150-1000N vertical load range.
        # They are NOT validated against TTC (Tyre Test Consortium) data for the specific
        # Shell Eco-Marathon tyre. Fy/Fx magnitudes carry ±30% uncertainty until replaced
        # with tyre-specific measured data. Fz input expected in kN (Fz/1000 applied by caller).
        # Reference form: Pacejka, H.B. (2012) Tyre and Vehicle Dynamics, 3rd ed., §4.3
        a = (1.65, -34.0, 1250.0, 3036.0, 12.8, 0.00501, -0.02103, 0.77394)
        b = (1.67272, -9.46, 1490.0, 30.0, 176.0, 0.08860, 0.00402, -0.06150, 0.20000)

        def calc_Fx_pure(lon_slip, Fz):
            #"""Calculates pure longitudinal force."""
            C = b[0]                                                          # Shape factor
            D = (b[1] * Fz**2 + b[2] * Fz)                                   # Peak factor
            BCD = (b[3] * Fz**2 + b[4] * Fz) * np.exp(-b[5] * Fz)            # Initial grip per unit lon_slip
            B_lon = BCD / (C * D) if (C * D) != 0 else 0                      # Mathematical scaling factor
            E = b[6] * Fz**2 + b[7] * Fz + b[8]                               # curvature factor
            return D * np.sin(C * np.arctan(B_lon * lon_slip - E * (B_lon * lon_slip - np.arctan(B_lon * lon_slip))))   # peak Fx force

        def calc_Fy_pure(alpha_deg, Fz):
            #"""Calculates pure lateral force."""
            C = a[0]
            D = (a[1] * Fz**2 + a[2] * Fz)
            # PAC89 stiffness factor for lateral
            self.p.K = a[3] * np.sin(2 * np.arctan(Fz / a[4]))                     # lateral/cornering stiffness
            B_lat = self.p.K / (C * D) if (C * D) != 0 else 0                      # Stiffness factor
            E = a[5] * Fz**2 + a[6] * Fz + a[7]
            return D * np.sin(C * np.arctan(B_lat * alpha_deg - E * (B_lat * alpha_deg - np.arctan(B_lat * alpha_deg))))   # peak Fy force

        def calc_Fy_combined():
            #"""Calculates the lateral force potential considering kremple diagram"""
            self.p.Fx_pure = calc_Fx_pure(lon_slip, Fz)
            self.p.Fy_pure = calc_Fy_pure(alpha_deg, Fz)

            # FIX #4: Scan over BOTH positive and negative slip [-30, 30] and take max of |Fx|.
            # V1 only scanned [0, 30] (positive slip / acceleration), which gave a wrong Fx_max
            # reference when lon_slip is negative (braking) and the tire has directional asymmetry.
            # Using abs() across the full slip range captures the true friction circle major axis.
            Fx_max = np.max([abs(calc_Fx_pure(s, Fz)) for s in np.linspace(-30, 30, 1000)])

            # Preventing negative square root
            rho_x = min(abs(self.p.Fx_pure / Fx_max), 1.0) if Fx_max > 0 else 0.0
            self.p.Fy_combined = self.p.Fy_pure * np.sqrt(1 - rho_x**2)   # from ellipse equation, a standard method for PAC89

        # Execute the calculation
        calc_Fy_combined()
        return self.p.Fy_combined

    def solve_cornering(self):
        a = (1.65, -34.0, 1250.0, 3036.0, 12.8, 0.00501, -0.02103, 0.77394)
        b = (1.67272, -9.46, 1490.0, 30.0, 176.0, 0.08860, 0.00402, -0.06150, 0.20000)
        #"""RA lateral force"""
        self.p.ra_Fy_required = self.p.m * self.p.ay * (self.p.l_f / self.p.wb)

        def calc_cornering_stiff(Fz):            # <N/deg> though KN is used as input
            Fz = Fz / 1000                       # convert Fz to KN
            #"""Calculates Pure Cornering stiffness."""
            return a[3] * np.sin(2 * np.arctan(Fz / a[4]))   # same as "K" lateral/cornering stiffness

        # FIX #3: Compute EFFECTIVE cornering stiffness K_eff = K_pure × √(1 − ρx²) to account
        # for the friction ellipse reduction under combined slip.
        # V1 used K_pure (no-slip) to back-calculate alpha_deg, then called kremple() which
        # applied the ellipse correction again — leading to an inconsistently low alpha_deg
        # and a distorted Fy curve estimate. The fix ensures the alpha_deg fed into kremple()
        # is the physically correct slip angle required under combined loading.
        def calc_Fx_pure_scalar(lon_slip, Fz_kN):
            #"""Single-point Fx evaluation (kN inputs) for K_eff correction."""
            C = b[0]
            D = (b[1] * Fz_kN**2 + b[2] * Fz_kN)
            BCD = (b[3] * Fz_kN**2 + b[4] * Fz_kN) * np.exp(-b[5] * Fz_kN)
            B = BCD / (C * D) if (C * D) != 0 else 0
            E = b[6] * Fz_kN**2 + b[7] * Fz_kN + b[8]
            return D * np.sin(C * np.arctan(B * lon_slip - E * (B * lon_slip - np.arctan(B * lon_slip))))

        def calc_Fx_max(Fz_kN):
            #"""Peak |Fx| over full slip range [-30, 30] used as friction ellipse major axis."""
            return np.max([abs(calc_Fx_pure_scalar(s, Fz_kN)) for s in np.linspace(-30, 30, 1000)])

        def calc_K_eff(Fz):
            #"""Effective cornering stiffness corrected for combined slip (friction ellipse)."""
            Fz_kN = Fz / 1000
            K_pure = calc_cornering_stiff(Fz)
            Fx_pure = calc_Fx_pure_scalar(self.p.lon_slip, Fz_kN)
            Fx_max = calc_Fx_max(Fz_kN)
            rho_x = min(abs(Fx_pure / Fx_max), 1.0) if Fx_max > 0 else 0.0
            return K_pure * np.sqrt(1 - rho_x**2)   # K_eff under combined slip

        K_eff_out = calc_K_eff(self.p.ra_Fz_out)
        K_eff_inn = calc_K_eff(self.p.ra_Fz_inn)
        # link the load shift due to roll, acc, inc and banking while cornering
        # now correctly uses K_eff so alpha_deg is consistent with kremple's friction ellipse
        self.p.alpha_deg = self.p.ra_Fy_required / (K_eff_out + K_eff_inn)

        self.p.ra_Fy_out = self.kremple((self.p.ra_Fz_out / 1000), self.p.lon_slip, self.p.alpha_deg)
        self.p.ra_Fy_inn = self.kremple((self.p.ra_Fz_inn / 1000), self.p.lon_slip, self.p.alpha_deg)

