    #"""Calculated dynamic wheel radius"""
    def dynamic_wheel(self, Fz):
        tire_amplitude_lim = Fz / self.p.k_ver
        r_dynamic = self.p.r_unloaded - tire_amplitude_lim
        tire_wavelength_lim = 2 * np.sqrt(self.p.r_unloaded ** 2 - r_dynamic ** 2)
        return (r_dynamic, tire_wavelength_lim)

    def static_wheel_parameters(self):
        static_wheel = self.dynamic_wheel(self.p.m_ra / 2 * 9.81)
        self.p.static_driven_tire_wavelength_lim = static_wheel[1]
        self.p.static_r_wheel = static_wheel[0]


