    #"""Moment fatigue about Z-axis"""
    def solve_moment_z(self, Fz, alpha_deg):   # according to PAC89, computes using pneumatic trail
        Fz = Fz / 1000
        c = (2.34000, 1.4950, 6.416654, -3.57403, -0.087737, 0.098410, 0.0027699, -0.0001151, 0.1000, -1.33329, 0.025501, -0.02357, 0.03027)
        C = c[0]
        D = c[1] * Fz ** 2 + c[2] * Fz
        BCD = (c[3] * Fz**2 + c[4] * Fz) * np.exp(-c[5] * Fz)
        B = BCD / (C * D)
        E = c[6] * Fz ** 2 + c[7] * Fz + c[8]
        Sh = c[9] * Fz + c[10]               # Angle offset <deg> Ply steer + conicity
        Sv = c[11] * Fz **2 + c[12] * Fz     # Moment offset <N·m> Residual conicity torque
        x = alpha_deg + Sh
        return D * np.sin(C * np.arctan(B * x - E * (B * x - np.arctan(B * x)))) + Sv

