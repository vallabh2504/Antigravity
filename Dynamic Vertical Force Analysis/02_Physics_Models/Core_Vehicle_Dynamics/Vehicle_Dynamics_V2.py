class VehicleDynamics:
    def __init__(self, params):
        self.p = params                                                 # vehicle_config EasyDict
        self.g = 9.81
        # Auto-calculate interdependent basic params
        self.p.wb = self.p.l_r + self.p.l_f                             # wheelbase
        self.p.m_ra = self.p.m * (self.p.l_f / self.p.wb)               # static total mass at RA
        self.p.m_fa = self.p.m * (self.p.l_r / self.p.wb)               # static total mass at FA
        self.p.m_ra_spr = self.p.m_spr * (self.p.l_f / self.p.wb)       # RA sprung mass
        self.p.m_fa_spr = self.p.m_spr * (self.p.l_r / self.p.wb)       # FA sprung mass
        # Dis b/w CG and Roll Axis
        self.p.del_h = self.p.cg_hei - self.p.l_r/self.p.wb * (self.p.fa_rp - self.p.ra_rp) - self.p.ra_rp

        # FIX #6 (superseded by M3): generate_road_profile() call removed from __init__.
